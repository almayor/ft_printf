#include "libft.h"

char	*ft_strsrch(const char *s, int (*f)(char))
{
	size_t	i;

	if (!s || !f)
		return (NULL);
	i = 0;
	while (s[i])
	{
		if (f(s[i]))
			return ((char *)(s + i));
		i++;
	}
	return (NULL);
}
